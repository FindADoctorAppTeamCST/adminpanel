» Opioid agonist maintenance treatment requires the presence of an
established and regulated national framework. It is characterized by the
prescription of long-acting opioid agonists (or partial agonists), such as
methadone or buprenorphine, generally on a daily, supervised basis.
There is strong evidence that agonist maintenance treatment with
methadone or buprenorphine effectively reduces illicit drug use, the
spread of HIV, mortality, and criminality, as well as improving physical
health, mental health, and social functioning.
» Monitoring: Medications used for opioid agonist maintenance treatment
are open to misuse and diversion, hence, programmes should use various
methods of limiting the risk of diversion, including supervised
